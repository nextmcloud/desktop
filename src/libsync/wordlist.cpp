int getRandomNumber(int max) {
    unsigned char d[8];
    RAND_bytes(d, 8);

    unsigned int num = 0;

    for (unsigned char c : d) {
        num = num << 8;
        num += c;
    }

    return static_cast<int>(num % max);
}

